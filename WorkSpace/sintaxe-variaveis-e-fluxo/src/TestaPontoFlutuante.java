
public class TestaPontoFlutuante {
	public static void main(String[] args) {
		double salarioMinimo = 1212.50;
		System.out.println("Salario minimo é: "+salarioMinimo);
		
		double divisao = 3.1415/2;
		System.out.println("Resultado da divisão de pí: "+divisao);
		
		int divisaoInt = 5/2;
		System.out.println("Resultada da divisão usando o tipo Int: "+divisaoInt);
		
		double divisaoDouble = 5/2;
		System.out.println("Resultado da divisão usando o tipo Double: "+divisaoDouble);
		
		double outraDivisaoDouble = 5.0/2;
		System.out.println("Outro resultado da divisao usando o tipo Double: "+outraDivisaoDouble);
		
	}
}
